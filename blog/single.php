                  <p style="font-family: Georgia;font-size: 18px;">The richest 85 people in the world own as much as the poorest half of humanity. This mere
                  statement takes a while to sink in, doesnâ€™t it? And the gap between rich and poor seems to
                  just be getting wider- 7 out of every 10 people, live in countries where economic inequality is
                  worse that it was thirty years ago.
                  To actually get a first-hand experience from someone on the issue of existing inequalities, I
                  interacted with Ramesh, who has been working as a domestic helper in one of the local
                  households since the last 16 years.
                  He was born and brought up in Giridih, a small village in Jharkhand. He initially talked about
                  his childhood and how he lived with three brothers and two sisters. The four brother had still
                  studied up to some class, but the daughters were not allowed to study all. He noted that times
                  have changed extensively since then.
                  According to him, very few inequalities exist within the village today, and only when one
                  compares it with the developed cities, can one notice the widening gap. He supports his
                  family which comprises of six people: two sons, two daughters, his wife and himself. When
                  he goes home, to his village, he helps out his wife in farming to earn some livelihood and
                  support the family. He added that there is sufficient water for us to drink and bathe, but not so
                  much to support extensive farming of crops like rice. Farming of such crops mostly depends
                  on monsoons. He also has a cow which provides them with milk for household purposes.
                  He very proudly told me that he is making all 4 of his children study, despite him being able
                  to study only till grade II due to limited resources and poverty. He told me that he even
                  bought a cycle for his elder son when he cleared his class X examinations. He added that
                  today, the girl boy ratio in the village is almost 1:1 and everyone is studying in the
                  Government schools nearby. There is no distinction on the basis of gender. There is also a
                  private hospital in the village, which tends to be a little expensive, but given that it is a
                  necessity, it is essential. Thus, he feels his village is also developing but just at a slower pace
                  as compared to the cities.
                  Inequalities are reducing within the village, but to achieve the SDG10, one has to work
                  towards reducing the economic gap which transcend geographical boundaries, like those
                  existing between villages and cities, and those among countries. </p>
